public class foreach {

    public static void main(String[] args) {

        int[] arr={4,5,6,88,9996,44,};
        for(int ar:arr){
            System.out.print(ar+",");
        }
        System.out.println();
        for (int i=0;i<arr.length;i++){

           System.out.print(arr[i]+",");
        }
    }


}
